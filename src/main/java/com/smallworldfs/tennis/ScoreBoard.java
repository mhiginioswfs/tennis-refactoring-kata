package com.smallworldfs.tennis;

public class ScoreBoard {

    private int score1;
    private int score2;

    public int getScore1() {
        return score1;
    }

    public int getScore2() {
        return score2;
    }

    public void incScore1() {
        score1++;
    }

    public void incScore2() {
        score2++;
    }
}
